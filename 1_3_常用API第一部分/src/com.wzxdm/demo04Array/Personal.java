package src.com.wzxdm.demo04Array;

public class Personal {
    private String name;
    private int age;

    public Personal() {
    }

    public Personal(String name, int age) {
        this.name = name;
        this.age = age;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }
}
