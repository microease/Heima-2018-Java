public class DataType {
    public static void main(String[] args) {
        System.out.println(1024);
        long num1 = 100;
        System.out.println(num1);
        double num2 = 2.5F;
        System.out.println(num2);
        // 强制类型转换
        int num3 = (int) 100L;
        System.out.println(num3);
        int num4 = (int) 3.5;
        System.out.println(num4);
    }
}
