public class Demo06Operator {
    public static void main(String[] args){
        int num1 = 10;
        System.out.println(num1);
        ++num1;
        System.out.println(num1);
        num1++;
        System.out.println(num1);
        int num2 = 20;
        System.out.println(num2++);
        System.out.println(++num2);
    }
}
